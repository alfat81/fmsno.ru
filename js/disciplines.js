document.addEventListener('DOMContentLoaded', () => {
    // Привязываем клик ко всем карточкам дисциплин
    document.querySelectorAll('.discipline-card').forEach(card => {
        // Добавляем подсказку, что карточка кликабельна
        card.setAttribute('role', 'button');
        card.setAttribute('tabindex', '0');
        card.setAttribute('aria-label', 'Открыть описание дисциплины');

        card.addEventListener('click', () => {
            const key = card.dataset.discipline;
            if (key) openDisciplineModal(key);
        });

        // Открывать по Enter для доступности
        card.addEventListener('keydown', (e) => {
            if (e.key === 'Enter' || e.key === ' ') {
                e.preventDefault();
                const key = card.dataset.discipline;
                if (key) openDisciplineModal(key);
            }
        });
    });

    // Закрытие модалки по Escape
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape') closeDisciplineModal();
    });
});

function openDisciplineModal(key) {
    const data = DISCIPLINES_DATA[key];
    if (!data) {
        console.warn('Дисциплина не найдена:', key);
        return;
    }

    // Если модал уже есть — удаляем
    const existing = document.getElementById('discipline-modal');
    if (existing) existing.remove();

    const paragraphsHTML = data.paragraphs.map(p => `<p>${p}</p>`).join('');
    const factsHTML = (data.facts || []).map(f => `
        <div class="discipline-fact">
            <i class="fas ${f.icon}"></i>
            <div class="discipline-fact-text">
                <div class="discipline-fact-label">${f.label}</div>
                <div class="discipline-fact-value">${f.value}</div>
            </div>
        </div>
    `).join('');

    const modal = document.createElement('div');
    modal.id = 'discipline-modal';
    modal.className = 'discipline-modal-overlay';
    modal.innerHTML = `
        <div class="discipline-modal">
            <button class="discipline-modal-close" aria-label="Закрыть">&times;</button>
            <div class="discipline-modal-hero" style="background-image: url('${data.image}');">
                <div class="discipline-modal-hero-overlay"></div>
                <h2 class="discipline-modal-title">${data.name}</h2>
            </div>
            <div class="discipline-modal-body">
                <p class="discipline-modal-subtitle">${data.shortDescription}</p>
                <div class="discipline-modal-text">
                    ${paragraphsHTML}
                </div>
                ${factsHTML ? `
                    <div class="discipline-modal-facts">
                        <h3 class="discipline-facts-title"><i class="fas fa-info-circle"></i> Ключевые факты</h3>
                        <div class="discipline-facts-grid">
                            ${factsHTML}
                        </div>
                    </div>
                ` : ''}
                <div class="discipline-modal-actions">
                    <a href="competitions.html" class="btn btn-primary">
                        <i class="fas fa-calendar-alt"></i> Соревнования по этой дисциплине
                    </a>
                    <a href="contacts.html" class="btn btn-secondary">
                        <i class="fas fa-question-circle"></i> Как начать заниматься
                    </a>
                </div>
            </div>
        </div>
    `;
    document.body.appendChild(modal);
    document.body.style.overflow = 'hidden';

    // Плавное появление
    requestAnimationFrame(() => modal.classList.add('active'));

    // Закрытие по клику на overlay или крестик
    modal.addEventListener('click', (e) => {
        if (e.target === modal || e.target.classList.contains('discipline-modal-close')) {
            closeDisciplineModal();
        }
    });
}

function closeDisciplineModal() {
    const modal = document.getElementById('discipline-modal');
    if (!modal) return;
    modal.classList.remove('active');
    setTimeout(() => {
        if (modal.parentNode) modal.parentNode.removeChild(modal);
        document.body.style.overflow = 'auto';
    }, 300);
}
